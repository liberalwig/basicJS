Math.pow(3,2); // output: 9 (제곱)

Math.round(10.6); // output: 11 (반올림)

Math.ceil(10.2); // output: 11 (윗쪽 정수)

Math.floor(10.2); // output: 10 (아래쪽 정수)

Math.sqrt(9); // output: 3 (제곱근)

Math.random(); // output: 1보다 작은 랜덤수

100 * Math.random(); // output: 100보다 작은 랜덤수

Math.round(100 * Math.random()); // output: 소수점 없는 100보다 작은 랜덤수